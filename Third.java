public class Third{
    public static void main(String[] args) {
        int num1 = 5, num2 = 6;
        System.out.println(num1);
        System.out.println(num2);
        // swaping the numbers
        int temp = num1;
        num1 = num2;
        num2 = temp;
       // numbers after the swaping
        System.out.println(num1);
        System.out.println(num2);
    }
}